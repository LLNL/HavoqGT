namespace rhh {
template <class Key>
class hash {
 public:
  uint64_t operator()(Key const &key, uint64_t const &seed = 123) const {
    return detail::utility::MurmurHash64A(&key, sizeof(Key), seed);
  }
};
} // namespace rhh

